<?php
// Arithmetic Operators Example

// Addition
$num1 = 10;
$num2 = 5;
$sum = $num1 + $num2;
echo "Addition: $num1 + $num2 = $sum <br>";

// Subtraction
$diff = $num1 - $num2;
echo "Subtraction: $num1 - $num2 = $diff <br>";

// Multiplication
$product = $num1 * $num2;
echo "Multiplication: $num1 * $num2 = $product <br>";

// Division
$quotient = $num1 / $num2;
echo "Division: $num1 / $num2 = $quotient <br>";

// Modulus (Remainder)
$remainder = $num1 % $num2;
echo "Modulus: $num1 % $num2 = $remainder <br>";

// Increment
$incrementedNum = $num1++;
echo "Increment: Original Value: $num1, Incremented Value: $incrementedNum <br>";

// Decrement
$decrementedNum = $num2--;
echo "Decrement: Original Value: $num2, Decremented Value: $decrementedNum <br>";
